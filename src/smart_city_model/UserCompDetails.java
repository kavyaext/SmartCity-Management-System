    public void loadDetails(){
        String url="jdbc:mysql://localhost:3306/smart_city";
        String password=datapass;
        String user="root";
        String sql;
        ResultSet rs;
        String tableName = "";
        switch(type){
            case 1: tableName = "eleccomplaints";break;
            case 2: tableName = "complaints";break;
            case 3: tableName = "roadcomp";break;
            case 4: tableName = "watercomp";break;
            case 5: tableName = "feedback";break;
        }
        try(Connection conn = DriverManager.getConnection(url,user,password);Statement stmt = conn.createStatement();) {
            DefaultTableModel m = (DefaultTableModel) this.jTable1.getModel();
            m.setRowCount(0);
            JTableHeader th = jTable1.getTableHeader();
            TableColumnModel tcm = th.getColumnModel();
            if(type == 5){
                TableColumn tc = tcm.getColumn(0);
                TableColumn tc1 = tcm.getColumn(2);
                tc.setHeaderValue("Area");
                tc1.setHeaderValue("Feedback");
                th.repaint();
                sql="SELECT * from "+tableName+" WHERE area = '"+area+"' and status = '"+status+"' and username = '"+name+"'";
                rs = stmt.executeQuery(sql);
                while(rs.next()){
                    m.addRow(new Object[] {
                        rs.getString("area"),
                        rs.getString("username"),
                        rs.getString("feedback")
                    });
                }
            }
            else{
            sql="SELECT * from "+tableName+" WHERE area = '"+area+"' and status = '"+status+"' and username = '"+name+"'";
            rs = stmt.executeQuery(sql);
            while(rs.next()){
                m.addRow(new Object[] {
                    rs.getString("name"),
                    rs.getString("address"),
                    rs.getString("reason")
                });
            }}
        }catch (Exception x) {
            JOptionPane.showMessageDialog(null,"Error 404 not found");
            x.printStackTrace();
        }
    }
